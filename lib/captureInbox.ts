// The App-Group capture inbox — the widget/intent → app write path (B-290, PR W3).
//
// The widget (W5) and its App Intents (W4) run in an extension process with no
// access to the app's SQLite file, so a Home Screen tap cannot write the record
// directly. Instead the extension appends ONE JSON FILE PER CAPTURE to the
// shared container's inbox (lib/appGroup.ts), and the app ingests those files
// into SQLite + the existing sync queue on the next foreground (hooks/useSync
// runs the ingest immediately before syncNow, so an ingested record rides that
// same cycle's push). The intent may ALSO attempt a direct Supabase REST write
// when online — the inbox is still written first and remains the source of
// truth for reconciliation: both paths use the SAME row ids (generated by the
// extension at tap time), so the local insert-if-absent + the id-keyed upsert
// converge on one row no matter which path lands first, and a record is only
// removed from the inbox once the app has durably applied it locally.
//
// Idempotency is the spine of the §4.1 "no lost taps" requirement: the ingest
// deletes a file only AFTER the rows are written, so a crash between apply and
// delete re-ingests the file next pass and the INSERT OR IGNORE no-ops. A tap
// is lost only if the extension's own file write fails — which the extension
// surfaces as a failed tap, never a silent one (W4/W5 contract).
//
// The no-garbage rule (spec D2) is enforced structurally: a meal/treat record
// MUST name a food_item_id — there is no "generic treat, edit later" shape to
// parse, so an unnamed capture cannot exist in this pipeline. (The W4
// 'bowl_topup' kind names the standing fact instead of a food — see
// captureRecord.ts.) And provenance is narrowed to the inbox surfaces
// ('widget'/'intent' — lib/loggedVia.ts), so the inbox can never forge an
// in-app write.

